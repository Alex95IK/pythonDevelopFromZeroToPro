yummy = 'This is something text '

# MULTIPLICATION
print(yummy * 2)

# UPPER REGISTER METHOD
print(yummy.upper())
# or
print('Something text '.upper())

# LOWER REGISTER METHOD
print(yummy.lower())
# or
print('Something text '.lower())

# SPLIT METHOD
print(yummy.split())
# or we can give custom split sign
print(yummy.split('s'))
